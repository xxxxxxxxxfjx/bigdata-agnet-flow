function panelStyle(color = 'rgba(148,163,184,0.18)') {
  return {
    borderRadius: 6,
    background: 'rgba(15,23,42,0.62)',
    border: `1px solid ${color}`,
    boxShadow: '0 12px 36px rgba(0,0,0,0.22)',
  }
}

